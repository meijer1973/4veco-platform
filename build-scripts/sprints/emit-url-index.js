#!/usr/bin/env node
// Emit reports/url-index.md — a single document that points an external
// research agent (notably a planning Claude in claude.ai) at every high-value
// raw-GitHub URL across the 4veco-platform and 4veco-lessen repositories.
// One fetch unlocks the rest of the surface.

const fs = require('fs');
const path = require('path');

const { parseRepoFromPackageJson, encodePath, buildRawUrl } = require('./emit-gate-bundle-urls.js');

// 4veco-lessen lives in a separate repo; record it explicitly so the index
// can cross-link without an ad-hoc package.json lookup in the sibling tree.
const LESSEN_OWNER = 'meijer1973';
const LESSEN_REPO = '4veco-lessen';

function fail(message) {
  console.error(`emit-url-index: ${message}`);
  process.exit(1);
}

function platformUrl(rel, owner, repo, branch) {
  return buildRawUrl(owner, repo, branch, rel);
}

function lessenUrl(rel, branch) {
  return `https://raw.githubusercontent.com/${LESSEN_OWNER}/${LESSEN_REPO}/${branch}/${encodePath(rel)}`;
}

function listGates() {
  const dir = path.join(process.cwd(), 'reports', 'review-gates');
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir, { withFileTypes: true })
    .filter((entry) => entry.isDirectory() && /^GATE-/.test(entry.name))
    .map((entry) => entry.name)
    .sort();
}

function buildBody(branch) {
  const { owner, repo } = parseRepoFromPackageJson();
  const platform = (rel) => platformUrl(rel, owner, repo, branch);
  const lessen = (rel) => lessenUrl(rel, branch);

  const lines = [];
  lines.push('# URL Index');
  lines.push('');
  lines.push(
    'Single-fetch entry point for external research agents (notably planning Claude ' +
      'instances in claude.ai whose `web_fetch` tool can only follow URLs that have ' +
      'appeared as literal `https://...` strings in context). Fetching this one file ' +
      'unlocks the rest of the platform and lessen surfaces. Generated by ' +
      '`build-scripts/sprints/emit-url-index.js`. Do not hand-edit.'
  );
  lines.push('');

  lines.push('## Research Agent Maps');
  lines.push('');
  lines.push(`- ${platform('RESEARCH_AGENT_MAP.md')}`);
  lines.push(`- ${platform('RESEARCH_AGENT_MAP_REFERENCES.md')}`);
  lines.push(`- ${lessen('RESEARCH_AGENT_MAP.md')}`);
  lines.push('');

  lines.push('## Research Agent Prompts');
  lines.push('');
  lines.push(`- ${platform('RESEARCH_AGENT_PROMPT.md')}`);
  lines.push(`- ${platform('RESEARCH_AGENT_PROMPT_REFERENCES.md')}`);
  lines.push(`- ${lessen('RESEARCH_AGENT_PROMPT.md')}`);
  lines.push('');

  lines.push('## Repo Operating Rules');
  lines.push('');
  lines.push(`- ${platform('AGENTS.md')}`);
  lines.push(`- ${platform('CLAUDE.md')}`);
  lines.push(`- ${platform('BUILD-PARAGRAPH.md')}`);
  lines.push(`- ${platform('BUILD-CHAPTER.md')}`);
  lines.push(`- ${lessen('AGENTS.md')}`);
  lines.push('');

  lines.push('## Roadmaps');
  lines.push('');
  lines.push(`- ${platform('references/reference-team-roadmap.md')}`);
  lines.push(`- ${platform('knowledge/platform-team-roadmap.md')}`);
  lines.push(`- ${platform('knowledge/three-month-roadmap.md')}`);
  lines.push(`- ${lessen('lessen-team-roadmap.md')}`);
  lines.push('');

  lines.push('## Owned Course Design');
  lines.push('');
  lines.push(`- ${platform('references/owned/course-blueprint-v5.md')}`);
  lines.push(`- ${platform('references/owned/course-blueprint-v5.meta.json')}`);
  lines.push(`- ${lessen('course_blueprint_v5.md')}`);
  lines.push('');

  lines.push('## Archived Owned Course Design');
  lines.push('');
  lines.push(`- ${platform('references/owned/course-blueprint-v4.md')}`);
  lines.push(`- ${platform('references/owned/course-blueprint-v4.meta.json')}`);
  lines.push(`- ${lessen('course_blueprint_v4.md')}`);
  lines.push('');

  lines.push('## Index Anchors (registries, evidence, target exercises)');
  lines.push('');
  lines.push(`- ${platform('references/machine/micro-teaching-units.json')}`);
  lines.push(`- ${platform('references/machine/micro-teaching-units.md')}`);
  lines.push(`- ${platform('references/machine/begrippen.json')}`);
  lines.push(`- ${platform('references/machine/begrippen.md')}`);
  lines.push(`- ${platform('references/external/exam-questions.json')}`);
  lines.push(`- ${platform('references/external/syllabus-eindtermen.json')}`);
  lines.push(`- ${platform('references/external/syllabus-eindtermen.md')}`);
  lines.push(`- ${platform('references/authored/course-target-exercises.json')}`);
  lines.push(`- ${platform('references/data/evidence-anchors.json')}`);
  lines.push(`- ${platform('references/data/alignment-graph.json')}`);
  lines.push(`- ${platform('references/data/owned-source-registry.json')}`);
  lines.push(`- ${platform('references/data/document_inventory.json')}`);
  lines.push(`- ${platform('references/data/source_manifest.json')}`);
  lines.push(`- ${platform('reports/internal-dashboard/dashboard-data.json')}`);
  lines.push('');

  lines.push('## Review-Gate Bundles');
  lines.push('');
  const gates = listGates();
  for (const gate of gates) {
    const bundleAbs = path.join(process.cwd(), 'reports', 'review-gates', gate, 'bundle-urls.md');
    if (!fs.existsSync(bundleAbs)) continue;
    const rel = path.posix.join('reports', 'review-gates', gate, 'bundle-urls.md');
    lines.push(`- ${platform(rel)}`);
  }
  lines.push('');

  return lines.join('\n');
}

function parseArgs(argv) {
  const args = argv.slice(2);
  let branch = 'main';
  let mode = 'write';
  for (let i = 0; i < args.length; i += 1) {
    const arg = args[i];
    if (arg === '--branch') {
      branch = args[i + 1];
      i += 1;
    } else if (arg.startsWith('--branch=')) {
      branch = arg.slice('--branch='.length);
    } else if (arg === '--check') {
      mode = 'check';
    }
  }
  return { branch, mode };
}

function emit({ branch }) {
  const body = buildBody(branch);
  const outPath = path.join(process.cwd(), 'reports', 'url-index.md');
  if (!fs.existsSync(path.dirname(outPath))) fail('reports/ directory missing');
  fs.writeFileSync(outPath, body, 'utf8');
  console.log(`wrote reports/url-index.md`);
}

function check({ branch }) {
  const expected = buildBody(branch);
  const outPath = path.join(process.cwd(), 'reports', 'url-index.md');
  if (!fs.existsSync(outPath)) {
    fail(`reports/url-index.md missing; run: node build-scripts/sprints/emit-url-index.js`);
  }
  const actual = fs.readFileSync(outPath, 'utf8');
  if (actual !== expected) {
    fail(
      `reports/url-index.md is stale. Regenerate: node build-scripts/sprints/emit-url-index.js`
    );
  }
  console.log('OK url-index: reports/url-index.md is current');
}

if (require.main === module) {
  const args = parseArgs(process.argv);
  if (args.mode === 'check') {
    check(args);
  } else {
    emit(args);
  }
}

module.exports = { buildBody };
