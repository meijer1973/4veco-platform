const fs = require('fs');
const path = require('path');

const { validateProcedureVisualRegistry } = require('./validate-procedure-visual-registry.js');

const REPO_ROOT = path.resolve(__dirname, '..', '..');
const REPORT_JSON_PATH = 'reports/json/procedure-visual-schema-status.json';
const REPORT_MD_PATH = 'reports/markdown/procedure-visual-schema-status.md';
const GATE_DIR = 'reports/review-gates/GATE-PV-G1-schema';
const GATE_PACKET_JSON_PATH = `${GATE_DIR}/review-packet.json`;
const GATE_PACKET_MD_PATH = `${GATE_DIR}/review-packet.md`;
const TECHNICAL_CLOSURE_JSON_PATH = `${GATE_DIR}/technical-closure.json`;
const TECHNICAL_CLOSURE_MD_PATH = `${GATE_DIR}/technical-closure.md`;

function repoPath(relPath) {
  return path.join(REPO_ROOT, relPath);
}

function writeJson(relPath, data) {
  const full = repoPath(relPath);
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, `${JSON.stringify(data, null, 2)}\n`);
}

function writeText(relPath, text) {
  const full = repoPath(relPath);
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, text);
}

function bulletList(items) {
  return items.map((item) => `- ${item}`).join('\n');
}

function commandList(commands) {
  return ['```bash', ...commands, '```'].join('\n');
}

function buildMarkdownReport(report) {
  const checks = report.checks.map((check) => `| ${check.id} | ${check.status} | ${check.detail} |`).join('\n');
  return `# Procedure-Visual Schema Status

Generated by: \`${report.generated_by}\`
Generated on: ${report.generated_on}
Status: \`${report.status}\`

## Summary

- Schema files: ${report.summary.schema_file_count}
- Registry overlay files: ${report.summary.registry_file_count}
- PV actions: ${report.summary.action_count}
- Visual types: ${report.summary.visual_type_count}
- Element types: ${report.summary.element_type_count}
- Real procedure templates: ${report.summary.procedure_template_count}
- Real visual states: ${report.summary.visual_state_count}
- Real unit-template links: ${report.summary.unit_template_link_count}
- Schema example templates: ${report.summary.schema_example_template_count}
- Schema example visual states: ${report.summary.schema_example_visual_state_count}
- Provisional operation references validated: ${report.summary.provisional_operation_reference_count}

## Boundary

No \`references/machine/\` Procedure-Visual registry exists or is authorized. The real PV registries under \`references/data/procedure-visual/\` are present but empty until PV.3. The examples in \`procedure-visual-vocab.json\` validate the schema contract only; they do not authorize student-facing projection.

Blocked product uses remain blocked: student diagnostics, adaptive routing, student-facing AI, automatic sequencing, mastery decisions, summative decisions, and unreviewed student-facing PV projection.

## PV-G1 Technical Checks

| Check | Status | Detail |
| --- | --- | --- |
${checks}

## Acceptance Commands

${commandList(report.acceptance_commands)}
`;
}

